int	ft_issorted(char *s1, char *s2)
{
	char	c1;
	char	c2;

	while (*s1 && *s2 && *s1 == *s2)
	{
		s1++;
		s2++;
	}
	c1 = tolower(*s1);
	c2 = tolower(*s2);
	if (c1 < c2)
		return (1);
	return (0);
}
